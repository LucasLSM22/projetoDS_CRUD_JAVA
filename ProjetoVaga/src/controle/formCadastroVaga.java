    private void preencherTabela() {
        vagas.getColumnModel().getColumn(0).setPreferredWidth(3);
        vagas.getColumnModel().getColumn(1).setPreferredWidth(3);
        vagas.getColumnModel().getColumn(2).setPreferredWidth(80);
        vagas.getColumnModel().getColumn(3).setPreferredWidth(15);
        vagas.getColumnModel().getColumn(4).setPreferredWidth(150);

        // Obtenha o modelo da tabela
        DefaultTableModel modelo = (DefaultTableModel) vagas.getModel();

        // Limpe todas as linhas existentes na tabela
        modelo.setRowCount(0);

        try {
            // Posicione o cursor antes do primeiro registro
            con_cliente.resultset.beforeFirst();

            // Itere sobre os resultados e adicione cada linha √† tabela
            while (con_cliente.resultset.next()) {
                modelo.addRow(new Object[]{
                    con_cliente.resultset.getString("ID_Vaga"),
                    con_cliente.resultset.getString("N_Vagas"),
                    con_cliente.resultset.getString("Cargo"),
                    con_cliente.resultset.getString("Salario"),
                    con_cliente.resultset.getString("Descricao")
                });
            }
        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, "Erro ao listar os dados da tabela: " + erro.getMessage(), "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);
        }
    }

    private void posicionarRegistro() {
        try {
            // Posicione o cursor no primeiro registro
            con_cliente.resultset.first();

            // Mostre os dados do registro atual
            mostrar_Dados();
        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, "N√£o foi poss√≠vel posicionar no primeiro registro: " + erro.getMessage(), "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);
        }
    }

    public void mostrar_Dados() {
        try {
            // Exiba os dados do registro atual nos campos de texto
            txtID_Vaga.setText(con_cliente.resultset.getString("ID_Vaga"));
            txt_N_Vagas.setText(con_cliente.resultset.getString("N_Vagas"));
            txt_Cargo.setText(con_cliente.resultset.getString("Cargo"));
            txt_Salario.setText(con_cliente.resultset.getString("Salario"));
            txt_Descricao.setText(con_cliente.resultset.getString("Descricao"));
        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, "N√£o foi poss√≠vel localizar dados: " + erro.getMessage(), "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);
        }

    }

